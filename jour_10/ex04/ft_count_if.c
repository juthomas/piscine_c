int		ft_count_if(char **tab, int (*f)(char*))
{
	int	u;
	int	i;

	i = 0;
	u = 0;
	while (tab[u] != 0)
	{
		if (f(tab[u]) == 1)
			i++;
		u++;
	}
	return (i);
}
